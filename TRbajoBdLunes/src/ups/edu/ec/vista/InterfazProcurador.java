    private void mostrar(){
        
        ResultSet rs = ControladorBD.getTabla("select codigo, cedula, nombre, apellido, "
                            +"direccion, numero,salario, fecha_nac from procurador");
        modelo.setColumnIdentifiers(new Object[]{"codigo","cedula","nombre","apellido"
        , "direccion", "numero","salario","fecha_nac" });
      
     try {
            while(rs.next()){
                modelo.addRow(new Object[]{rs.getString("codigo"),rs.getString("cedula")
                ,rs.getString("nombre"),rs.getString("apellido"),rs.getString("direccion")
                ,rs.getString("numero"),rs.getDouble("salario"),rs.getString("fecha_nac")});
            }
            jTableCliente.setModel(modelo);
        } catch (Exception e) {
            System.err.println(e);
        }
}
